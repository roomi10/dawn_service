package com.example;

public class Answer {
	public int id;
    public int next;
    public int mainId;
    public String answer;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNext() {
        return next;
    }

    public void setNext(int next) {
        this.next = next;
    }

    public int getMainId() {
        return mainId;
    }

    public void setMainId(int main_id) {
        this.mainId = main_id;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }
    public Answer() {}



}



